public class professor {
    private String professor_id;
    private String professor_name;
    private String professor_email_id;
    private String course_id;
    private String professor_pwd;

    public String getProfessor_id() {
        return professor_id;
    }

    public void setProfessor_id(String professor_id) {
        this.professor_id = professor_id;
    }

    public String getProfessor_name() {
        return professor_name;
    }

    public void setProfessor_name(String professor_name) {
        this.professor_name = professor_name;
    }

    public String getProfessor_email_id() {
        return professor_email_id;
    }

    public void setProfessor_email_id(String professor_email_id) {
        this.professor_email_id = professor_email_id;
    }

    public String getCourse_id() {
        return course_id;
    }

    public void setCourse_id(String course_id) {
        this.course_id = course_id;
    }

    public String getProfessor_pwd() {
        return professor_pwd;
    }

    public void setProfessor_pwd(String professor_pwd) {
        this.professor_pwd = professor_pwd;
    }
}
